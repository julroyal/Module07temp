public class partA {
    public static void main(String[] args) {
        int x = 0;
         for(x = 0; x <= 30; x++) {
            System.out.println(x);
        }
         for(x = 30; x >= 0; x--) {
             System.out.println(x);
         }
         for(x = 0; x <= 18; x+=3) {
             System.out.println(x);
         }
         for(x = 10; x >= 0; x-=2) {
             System.out.println(x);
         }
    }
}